/* Function to initialize both TX and RX messages with the default data values.
 * After initialization of the messages, the TX message is transmitted.
 */
void transmitCanMessage_CAN0(void)
{
    /* Initialization of the RX message with the default configuration */
    IfxCan_Can_initMessage(&g_mcmcan0.rxMsg);
    /* Invalidation of the RX message data content */
    memset((void *)(&g_mcmcan0.rxData[0]), INVALID_RX_DATA_VALUE, MAXIMUM_CAN_DATA_PAYLOAD * sizeof(uint32));

    /* Initialization of the TX message with the default configuration */
    IfxCan_Can_initMessage(&g_mcmcan0.txMsg);

//    /* Define the content of the data to be transmitted */
//    g_mcmcan0.txData[0] = TX_DATA_LOW_WORD;
//    g_mcmcan0.txData[1] = TX_DATA_HIGH_WORD;
    /* Modify the content of the data to be transmitted at each cycle */
    g_mcmcan0.txData[0] += 1;
    g_mcmcan0.txData[1] += 1;

    /* Set the message ID that is used during the receive acceptance phase */
    g_mcmcan0.txMsg.messageId = CAN0_MESSAGE_ID;

    /* Send the CAN message with the previously defined TX message content */
    while( IfxCan_Status_notSentBusy ==
           IfxCan_Can_sendMessage(&g_mcmcan0.canSrcNode, &g_mcmcan0.txMsg, &g_mcmcan0.txData[0]) )
    {
    }

    /* Just to indicate that the CAN message has been transmitted by toggling an LED */
    toggleLED1();
}

